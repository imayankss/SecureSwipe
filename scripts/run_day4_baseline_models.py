"""Day 4 orchestration script for the Credit Card Fraud Detection &
Risk Scoring System.

This script trains baseline models (Dummy, Logistic Regression, Random
Forest) on the Day 3 processed training data and evaluates them on the
validation set only. The test set is intentionally left untouched on
Day 4; it is reserved for final evaluation later in the project.

Run from the repository root:

    python3 scripts/run_day4_baseline_models.py
"""

